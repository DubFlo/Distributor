package vendingmachine.ui;

import javax.swing.BorderFactory;
import javax.swing.JButton;
import javax.swing.SwingConstants;

import vendingmachine.PictureLoader;
import vendingmachine.components.Drink;

public class DrinkJButton extends JButton {

  private static final long serialVersionUID = 1L; // generated by Eclipse
  private final Drink drink;

  public DrinkJButton(Drink drink) {
    super();
    this.drink = drink;
    setText(drink.getName());
    setIcon(PictureLoader.DRINK_BUTTON);
    setBorder(BorderFactory.createEmptyBorder());
    setContentAreaFilled(false);
    setHorizontalTextPosition(SwingConstants.CENTER);
    setVerticalTextPosition(SwingConstants.CENTER);
  }

  public Drink getDrink() {
    return this.drink;
  }

}