package vendingmachine.ui;

import javax.swing.BorderFactory;
import javax.swing.JButton;
import javax.swing.SwingConstants;

import vendingmachine.Drink;
import vendingmachine.PictureLoader;

/**
 * The DrinkJButton class extends JButton and only adds a Drink attribute.
 * 
 * @see Drink
 */
public class DrinkJButton extends JButton {

  private static final long serialVersionUID = 1L; // generated by Eclipse
  
  private final Drink drink;

  public DrinkJButton(Drink drink) {
    super();
    this.drink = drink;
    this.setText(drink.getName());
    if (PictureLoader.DRINK_BUTTON != null) {
      this.setIcon(PictureLoader.DRINK_BUTTON);
      this.setBorder(BorderFactory.createEmptyBorder());
      this.setContentAreaFilled(false);
      this.setHorizontalTextPosition(SwingConstants.CENTER);
    }
  }

  public Drink getDrink() {
    return this.drink;
  }

}