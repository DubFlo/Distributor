package vendingmachine.ui;

import java.io.File;
import javax.swing.*;
import vendingmachine.components.*;

public class DrinkJButton extends JButton {

	private static final long serialVersionUID = 1L; //generated by Eclipse
	private Drink drink;

	public DrinkJButton(Drink drink) {
		super();
		this.drink = drink;
		setText(drink.getName());
		setIcon(new ImageIcon("src"+File.separator+"resources"+File.separator+"redButton.png"));
		setBorder(BorderFactory.createEmptyBorder());
		setContentAreaFilled(false);
		setHorizontalTextPosition(JButton.CENTER);
		setVerticalTextPosition(JButton.CENTER);
	}

	public Drink getDrink() {
		return this.drink;
	}

}